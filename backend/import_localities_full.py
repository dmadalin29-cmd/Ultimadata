async def main():
    client = AsyncIOMotorClient(MONGO_URL)
    db = client[DB_NAME]
    
    localities_data = await fetch_localities()
    
    # Clear existing
    await db.localitati.delete_many({})
    
    all_localities = []
    for judet_code, localities in localities_data.items():
        for loc in localities:
            all_localities.append({
                "judet_code": judet_code,
                "name": loc["name"],
                "type": loc["type"],
                "lat": loc["lat"],
                "lng": loc["lng"],
                "search_name": loc["name"].lower().replace("ă", "a").replace("â", "a").replace("î", "i").replace("ș", "s").replace("ț", "t")
            })
    
    if all_localities:
        await db.localitati.insert_many(all_localities)
        print(f"Inserted {len(all_localities)} localities")
    
    # Create indexes
    await db.localitati.create_index("judet_code")
    await db.localitati.create_index("name")
    await db.localitati.create_index("search_name")
    await db.localitati.create_index([("name", "text")])
    
    client.close()
    print("Done!")

if __name__ == "__main__":
    asyncio.run(main())
